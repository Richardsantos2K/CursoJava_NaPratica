package Comandos_Instrucoes_java;

public class SwitchCase {
    public static void main(String[]args){

        int opcao = 1;

        switch (opcao){

            case 1:
                System.out.println("Ola, mundo");
                break;
            case 2:
                System.out.println("Mundo");
                break;
            case 3:
                System.out.println("Ola");
                break;
        }


    }

}
